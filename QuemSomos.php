    <h3>O INICIO</h3>
    <P>
    José Tião da Silva conhecido mais com Seu Zé e sua esposa Maria Aparecida da Silva "Dona Maria"
Mesmo sem saber nada de empreendedorismos fundaram a maior fábrica de doce caseiro da américa latina
O "Zé & Dona Maria", mas ninguém começa tão grande assim, antes de serem a maior e melhor eram apenas conhecidos
Em sua cidade Tiradentes localizada em Minas Gerais, no começo era algo simples feito na cozinha de casa,
Enquanto Dona Maria fazia suas receitas e Seu Zé pegava um isopor e saia para vender, e assim ambos sustentavam a família.
Tinham apenas 1 filho o José Carlos Benedito da Silva de 18 anos, uma família simples mas que deseja algo melhor dia a pós dia,
Seu Zé e dona Maria queriam muito que seu filho fizesse faculdade, pois era um menino esforçado e dedicado nos estudos
E por ambos não terem nem concluído o ensino médio desejavam de coração que seu único filho realizasse o sonho dele de cursa Administração.
E ai foi que seu Zé e Dona Maria começaram a se matar de trabalhar por esse sonho, mas eles sabiam que estavam mudando não só a vida de seu filho mas a deles próprios.

     </P>
     <H3>A MUDANÇA E O SUCESSO</H3>
     <p>
     Em 1985 seu filho José Carlos Benedito se formou após muito esforço do Seu Zé e dona Maria, e foi neste ano que tudo mudou para aqueles
Que mais sonharam com esse sonho, José Carlos decidiu que iria retribuir o esforço dos pais e foi ai que ele fundou junto com Senhor José Tião da Silva e dona Maria Aparecida da Silva
A Empresa Zé & Dona Maria, mas ainda tinha um longo chão a percorrer, mas não desistiram e foram atras de um empréstimo feito no banco
E de lá sairão devendo até "as cuecas", mas dia após dia a empresa foi pegando fama na região, no pais e até que atingiu a américa latina.
Com mais de 30 fábricas espalhadas pelo Brasil e 3 no exterior uma sendo no Chile, a outra na argentina e a terceira no México. Seu Zé com 90 anos e dona Maria com 91 anos
Não imaginavam que depois de tantas lutas atingiram esse patamar, mas a essência não mudou, a qualidade só melhorou e o que eles fizeram pelo filho deles conseguiram fazer que tantos outros "Zés e Marias" por ai
Fizesse o mesmo pelos seus filhos.

